/**
 * 获取指定城市和难度的随机题目
 * @param {string} cityName - 城市名称
 * @param {string} difficulty - 难度：'普通', '进阶', '挑战'
 * @returns {Object} 题目对象 {question, options, answer}
 */
function getCityQuestion(cityName, difficulty) {
  // 如果城市存在题库，使用城市题库，否则使用默认题库
  const cityQuestions = CITY_QUESTIONS[cityName] || CITY_QUESTIONS['DEFAULT'];
  const questions = cityQuestions[difficulty] || CITY_QUESTIONS['DEFAULT'][difficulty];

  // 随机选择一道题
  const randomIndex = Math.floor(Math.random() * questions.length);
  return questions[randomIndex];
}

/**
 * 检查城市是否有专属题库
 * @param {string} cityName - 城市名称
 * @returns {boolean} 是否有专属题库
 */
function hasCityQuestions(cityName) {
  return CITY_QUESTIONS.hasOwnProperty(cityName) && cityName !== 'DEFAULT';
}

